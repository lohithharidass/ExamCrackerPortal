<h2>Tamil Nadu Teacher Eligibility Test (TNTET)</h2>
<h4>Purpose by this exam:</h4>
<p>The TNTET is conducted to recruit eligible candidates as teachers for Primary (Classes I-V) and Upper Primary (Classes VI-VIII) in government and aided schools in Tamil Nadu.<p>
<h4>Eligibility criteria for this exam:</h4>
<p>For Primary Teachers (Classes I-V):</p>
<p>Minimum educational qualification: Higher Secondary (or equivalent) with at least 50% marks and passed or appearing in the final year of a 2-year Diploma in Elementary Education (D.El.Ed).</p>
<p>(or) A Bachelor's degree with at least 50% marks and B.Ed.</p>
<p>For Upper Primary Teachers (Classes VI-VIII):</p>
<p>Bachelor's degree and passed or appearing in the final year of a 2-year Diploma in Elementary Education (D.El.Ed) or B.Ed.</p>
<h4>Selection Process:</h4>
<p>Written Examination: Consists of two papers, Paper I for Primary and Paper II for Upper Primary.</p>
<h4>General Exam Syllabus:</h4>
<p>Paper I (Primary Teachers):</p> 
<p>Child Development and Pedagogy</p> 
<p>Language I (Tamil)</p>
<p>Language II (English)</p>
<p>Mathematics</p>
<p>Environmental Studies</p>
<p>Paper II (Upper Primary Teachers):</p>
<p>Child Development and Pedagogy</p>
<p>Language I (Tamil)</p>
<p>Language II (English)</p>
<p>Mathematics and Science OR Social Studies (depending on the subject chosen by the candidate)</p>
<h4>Pattern:</h4>
<p>Total Marks: 150 marks for both Paper I and Paper II</p>
<p>Total Questions: 150 objective-type questions (1 mark per question)</p>
<p>Duration: 3 hours</p>
<p>Physical Test: Qualifying</p>
<p>Qualifying Marks: 60% for general category and 55% for reserved categories.</p>

