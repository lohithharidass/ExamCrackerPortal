<p>Educational Qualification:</p> 
<p>A Bachelor's degree from a recognized university (any discipline). Final year students awaiting results are also eligible to apply.</p>
<h4>Selection Process:</h4>
<p>Written Examination: Objective type (includes Tamil language).</p>
<p>Physical Measurement Test (PMT): Candidates must meet the required height and chest (for males) standards.</p>
<p>Endurance Test (ET): Candidates must complete a specified distance run.</p>
<p>Physical Efficiency Test (PET): Events like running, long jump, and high jump.</p>
<p>Viva-Voce: An interview conducted after clearing all previous stages.</p>
<p>Medical Examination: Medical fitness is assessed after all other stages are completed.</p>
<h4>General Exam Syllabus:</h4> 
<p>Part I (Tamil Language Test): Mandatory test to assess Tamil language proficiency (for non-exempt candidates).</p> 
<p>Part II (General Knowledge):</p>
<p>General Studies: Indian Polity, History, Geography, Economics, Science, and Technology.</p>
<p>Psychology: Reasoning, Logical Analysis, Numerical Analysis, and Communication Skills.</p>
<h4>Pattern:</h4>
<p>Total Marks: 100 marks</p>
<p>Written Exam: 70 marks</p>
<p>Psychology: 30 marks</p>
<p>Viva-Voce: 10 marks</p>
<p>Physical Efficiency Test: 15 marks</p>
<p>Special Marks: 5 marks (for sports, NCC, etc.).</p>
<h4>Physical Standards:</h4>
<p>Male:</p>
<p>Height: 170 cm (General), 167 cm (SC/ST)</p>
<p>Chest: 81 cm (unexpanded) and 86 cm (expanded)</p>
<p>Female:</p>
<p>Height: 159 cm (General), 157 cm (SC/ST)</p>
<p>Chest: Not applicable.</p>
