<h2>TANCET for MBA </h2>
<h4>Eligibility criteria for this exam:</h4>
<p>Educational Qualification:</p>
<p>A Bachelor's degree (in any discipline) with a minimum of 50% marks (45% for reserved categories) from a recognized university.The degree must be of at least 3 years of study.</p>
<h4>General Exam Syllabus:</h4>
<p>Quantitative Ability: Basic mathematics, arithmetic, algebra, geometry, statistics, and probability.</p> 
<p>Logical Reasoning: Data interpretation, series completion, puzzles, and analogy.</p> 
<p>English Comprehension: Vocabulary, grammar, sentence correction, and comprehension passages.</p>
<p>General Awareness: Basic current affairs, economic, business-related topics.</p>
<h4>Pattern:</h4>
<p>Total Questions: 100 objective-type questions</p>
<p>Duration: 2 hours</p>
<p>Marking Scheme: 1 mark for each correct answer, and a penalty of 1/3 mark for each wrong answer.</p>

