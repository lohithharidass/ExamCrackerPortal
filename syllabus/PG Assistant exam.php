<h4>Pattern:</h4>
<p>Total Marks: 150 marks </p>
<p>Subject Knowledge: 110 marks</p>
<p>Duration: 3 hours</p>
<p>Education Methodology: 30 marks</p>
<p>General Knowledge: 10 marks</p>
<p>Duration: 3 hours</p>
<p>Minimum Qualifying Marks: 50% for general category, 45% for BC, 40% for MBC, SC, ST.</p>

