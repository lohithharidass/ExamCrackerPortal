<h2>PG Assistant Exam</h2>
<h4>Purpose by this exam:</h4>
<p>This exam is conducted to recruit Post Graduate Assistants for government higher secondary schools in Tamil Nadu.<p>
<h4>Eligibility criteria for this exam:</h4>
<p>Educational Qualification:</p>
<p>A Master's degree in the concerned subject with at least 50% marks.</p>
<p>A B.Ed degree from a recognized institution is mandatory.</p>
<h4>Selection Process:</h4>
<p>Written Exam: Objective type.</p>	
<p>Certificate Verification: Shortlisted candidates undergo document verification after clearing the exam.</p>
<h4>General Exam Syllabus:</h4>
<p>The syllabus varies based on the subject for which the candidate is applying, covering subjects such as Tamil, English, Mathematics, Physics, Chemistry, History, Geography, Economics, Commerce, etc.</p> 
<p>Subject knowledge (concerned subject)</p> 
