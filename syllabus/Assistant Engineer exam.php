<h2>TANGEDCO Assistant Engineer (AE) Exam</h2>
<h4>Posts that can be achieved by this exam:</h4>
<p>Assistant Engineer (Electrical)<p>
<p>Assistant Engineer (Civil)</p>
<p>Assistant Engineer (Mechanical)</p>
<h4>Eligibility criteria for this exam:</h4>
<p>Age: 18–30 years (General category), relaxations apply for reserved categories (SC/ST/OBC).</p>
<p>Educational Qualification:</p> 
<p>Assistant Engineer (Electrical): B.E./B.Tech in Electrical and Electronics Engineering (EEE) or equivalent from a recognized university.</p>
<p>Assistant Engineer (Civil): B.E./B.Tech in Civil Engineering or equivalent.</p>
<p>Assistant Engineer (Mechanical): B.E./B.Tech in Mechanical Engineering or equivalent.</p>
<h4>Selection Process:</h4>
<p>The exam is divided into three parts:</p>
<p>Part I: Engineering Mathematics (for engineering graduates) – 20 questions.</p>
<p>Part II: Basic Engineering & Sciences – 35 questions.</p>
<p>Part III: Subject-specific (Electrical, Civil, Mechanical) – 60 questions.</p>
<p>Topics for Part III (Electrical/Mechanical/Civil):</p>
<p>Electrical: Circuit Theory, Electrical Machines, Power Systems, Control Systems, Electrical Measurements, Power Electronics, etc.</p>
<p>Mechanical: Engineering Mechanics, Thermodynamics, Fluid Mechanics, Design of Machine Elements, Heat Transfer, etc.</p>
<p>Civil: Strength of Materials, Structural Engineering, Geotechnical Engineering, Environmental Engineering, Surveying, etc.</p>
<h4>General Exam Syllabus:</h4> 
<p>General Studies, Aptitude and Mental Ability, Current Events,</p> 
<p>History, Indian Polity, Geography, Economy, Science, and Technology.</p>
<h4>Pattern:</h4>
<p>Total Questions: 100 questions</p>
<p>Total Marks: 100 marks (1 mark per question)</p>
<p>Duration: 2 hours</p>
<p>Negative Marking: No negative marking</p>


