<h2>TANGEDCO Technical Assistant Exam</h2>
<h4>Posts that can be achieved by this exam:</h4>
<p>Technical Assistant (Electrical)<p>
<p>Technical Assistant (Mechanical)</p>
<h4>Eligibility criteria for this exam:</h4>
<p>Age: 18–30 years (General category), relaxations apply for reserved categories (SC/ST/OBC).</p>
<p>Educational Qualification:</p> 
<p>Technical Assistant (Electrical): Diploma in Electrical and Electronics Engineering (EEE) or equivalent from a recognized institution.</p>
<p>Technical Assistant (Mechanical): Diploma in Mechanical Engineering or equivalent.</p>
<h4>Selection Process:</h4>
<p>Written Exam: Objective type (Computer-based test).</p>
<p>Document Verification: After clearing the written exam, candidates are called for document verification.</p>
<h4>General Exam Syllabus:</h4> 
<p>The exam consists of three parts:
<p>Part I: General Tamil and English (20 questions)</p> 
<p>Part II: Basic Engineering (Electrical/Mechanical) – 35 questions</p>
<p>Part III: Subject-specific (Electrical/Mechanical) – 60 questions</p>
<p>Topics for Part III:</p>
<p>Electrical: Electrical Machines, Circuit Theory, Power Systems, Electrical Measurements, Control Systems, etc.</p>
<p>Mechanical: Thermodynamics, Fluid Mechanics, Engineering Mechanics, Strength of Materials, Machine Design, etc.</p>
<h4>Pattern:</h4>
<p>Total Questions: 100 questions</p>
<p>Total Marks: 100 marks (1 mark per question)</p>
<p>Duration: 2 hours</p>
<p>Negative Marking: No negative marking</p>


